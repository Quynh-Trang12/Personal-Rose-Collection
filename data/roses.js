/**
 * Get all roses across all categories
 * @returns {Array} Combined array of all roses
 */
export function getAllRoses() {
    return [
        ...bushRosesBelowOneMeter,
        ...bushRosesAroundOneMeter,
        ...bushRosesAboveOneMeter,
        ...climbingRoses,
    ];
}

/**
 * Find a rose by ID
 * @param {string} roseId - The ID of the rose to find
 * @returns {Object|null} The rose object or null if not found
 */
export function findRoseById(roseId) {
    return getAllRoses().find(rose => rose.id === roseId) || null;
}
